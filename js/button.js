

function show1() {
    var a = document.querySelectorAll(".common-how-it-work-class");
    a.forEach((ele) => {
        ele.style.border = "0.1rem solid var(--dark-grey)"
    })
    a[0].style.border = "0.1rem solid var(--bright-blue)";

    var b = document.querySelectorAll(".common-how-it-work-class .number");
    b.forEach((ele) => {
        ele.style.fontSize = "1.1rem";
        ele.style.color = "var(--light-grey)"
    })
    b[0].style.fontSize = "1.2rem";
    b[0].style.color = "var(--bright-blue)";

    var c = document.querySelectorAll(".common-how-it-work-class .text");
    c.forEach((ele) => {
        ele.style.fontSize = "1.2rem";
        ele.style.color = "var(--light-grey)"
    })
    c[0].style.fontSize = "1.3rem";
    c[0].style.color = "var(--dark-blue)";

    var d = document.querySelectorAll(".how-it-works-img-container");
    d.forEach((ele) => {
        ele.style.display = "none";
    })
    d[0].style.display = "block";

    // var d = document.querySelectorAll(".another-1");
    // d.forEach((ele) => {
    //     ele.style.display = "none";
    // })
    // d[0].style.display = "block";
}




